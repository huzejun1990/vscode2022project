#include <iostream>
using namespace std;

int main() {

    //转义字符

    //换行符 \n

    cout << "hello world\n";

    // 反斜杠 \\

    cout << "\\" << endl;

    // 水平制表符 \t 作用可以整齐的输出数据

    cout << "aaaa hello world" << endl;
    cout << "aa hello world" << endl;
    cout << "aaaaaa hello world" << endl;

    system("pause");

    return 0;
}
